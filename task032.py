# Задача 32: Определить индексы элементов массива (списка), значения которых принадлежат заданному диапазону 
# (т.е. не меньше заданного минимума и не больше заданного максимума)

# Ввод данных
array = list(map(int, input("Введите элементы массива, разделённые пробелами: ").split()))
min_value = int(input("Введите минимальное значение диапазона: "))
max_value = int(input("Введите максимальное значение диапазона: "))

# Создание пустого списка для хранения индексов
indexes = []

# Проход по элементам массива и проверка условия принадлежности диапазону
for index, value in enumerate(array):
    if min_value <= value <= max_value:
        indexes.append(index)

# Вывод списка индексов элементов, удовлетворяющих условию
print("Индексы элементов массива, принадлежащих заданному диапазону:")
for index in indexes:
    print(index)