# Задача 22: Даны два неупорядоченных набора целых чисел (может быть, с повторениями). 
# Выдать без повторений в порядке возрастания все те числа, которые встречаются в обоих наборах.
# Пользователь вводит 2 числа. n — кол-во элементов первого множества. m — кол-во элементов второго множества. 
# Затем пользователь вводит сами элементы множеств.

# Ввод количества элементов в каждом наборе
n = int(input("Введите количество элементов в первом наборе: "))
m = int(input("Введите количество элементов во втором наборе: "))

# Ввод и обработка элементов первого набора
first_set = set()
print("Введите элементы первого набора:")
for _ in range(n):
    element = int(input())
    first_set.add(element)

# Ввод и обработка элементов второго набора
second_set = set()
print("Введите элементы второго набора:")
for _ in range(m):
    element = int(input())
    second_set.add(element)

# Находим пересечение множеств и выводим результат
result = sorted(list(first_set.intersection(second_set)))
print("Пересечение множеств:", result)
